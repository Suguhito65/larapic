     * Display the specified resource.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function show(User $user)
    {
        $user = User::
        $posts = Post::where('user_id', $user->id)
                ->orderBy('created_at', 'desc')
                ->paginate(3);
        return view('users.show', [
            'user' => $user,
            'posts' => $posts
        ]);
    }

